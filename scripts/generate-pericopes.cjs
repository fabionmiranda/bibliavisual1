// ── Prefixos de livros em referências combinadas ─────────────────────────────
const REF_PREFIX_MAP = {
  '1S': '1Samuel', '2S': '2Samuel',
  '1K': '1Kings',  '2K': '2Kings',
  '1Ch': '1Chronicles', '2Ch': '2Chronicles',
  'Ezr': 'Ezra', 'Ne': 'Nehemiah',
};

function detectBook(ref) {
  const s = ref.replace(/\s+/g, '');
  for (const [prefix, book] of Object.entries(REF_PREFIX_MAP)) {
    if (s.startsWith(prefix)) return book;
  }
  return null;
}

function stripBookPrefix(ref) {
  return ref.replace(/\b(1S|2S|1K|2K|1Ch|2Ch|Ezr|Ne)\b/g, '').trim();
}

// Formata referência exata do XLS: "1:1-31 2:1-4a" → "1.1-31; 2.1-4a"
function formatRefPT(ref) {
  if (!ref) return '?';
  // Remove prefixos de livro (1S, 2K, etc.) e whitespace extra
  let r = ref.replace(/\b(1S|2S|1K|2K|1Ch|2Ch|Ezr|Ne)\b/g, ' ').replace(/\s+/g, ' ').trim();
  // Múltiplos segmentos separados por espaço → separar com ";"
  const segments = r.split(/\s+(?=\d+:)/);
  return segments
    .map(s => s.trim().replace(/:/g, '.'))  // colons → dots
    .join('; ');
}

function parseCapitulos(ref) {
  return formatRefPT(ref);
}

function parseVersiculo(ref, abrev) {
  // Pega só o primeiro segmento do ref para o versículo-chave
  const clean = stripBookPrefix(ref).trim();
  const m = clean.match(/^(\d+):(\d+)/);
  return m ? `${abrev} ${m[1]}.${m[2]}` : `${abrev} 1.1`;
}

